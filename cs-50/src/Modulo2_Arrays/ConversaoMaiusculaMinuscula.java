package Modulo2_Arrays;

public class ConversaoMaiusculaMinuscula {

	public static void main(String[] args) {
		
		
		char char1 = 'a';
		char char2 = 'B';
		
		char char1UppperCase = Character.toUpperCase(char1);
		char char2LowerCase = Character.toLowerCase(char2);
		
		
		System.out.println(char1 + " - " + char1UppperCase);
		
		System.out.println(char2 + " - " + char2LowerCase);

		
		
	}

}
